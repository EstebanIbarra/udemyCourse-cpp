/**
 * @brief Prints and returns the result of the sum of two numbers
 * 
 * @param number1 
 * @param number2 
 * @return int 
 */
int sum(int number1, int number2);

/**
 * @brief Prints and returns the substraction of the second number from the first one if it's a positive result
 * 
 * @param number1 
 * @param number2 
 * @return int 
 */
int sub(int number1, int number2);

/**
 * @brief Prints and returns the result of the product of two numbers
 * 
 * @param number1 
 * @param number2 
 * @return int 
 */
int mul(int number1, int number2);

/**
 * @brief Prints and returns the result of the division between the first and the second numbers without decimals
 * 
 * @param number1 
 * @param number2 
 * @return int 
 */
int division(int number1, int number2);

/**
 * @brief Prints and returns the remainder of the division between the first and second numbers
 * 
 * @param number1 
 * @param number2 
 * @return int 
 */
int mod(int number1, int number2);

